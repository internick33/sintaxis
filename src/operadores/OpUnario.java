package operadores;

public class OpUnario {
	public static void main(String [] args) {
		
		int a = 100;
		int b = 50;
		
		System.out.println("a vale: " + a);
		System.out.println("b vale: " + b);
		
		a++;
		b--;
		
		System.out.println("a vale: " + a);
		System.out.println("b vale: " + b);
		
	}
}
